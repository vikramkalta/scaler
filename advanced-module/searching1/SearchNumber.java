    public static int solve(ArrayList<Integer> A, int B) {
        int len = A.size();
        if (B < A.get(0)) {
            return 0;
        }
        if (B > A.get(len - 1)) {
            return len;
        }
        int ans = -1;
        int l = 0, r = len - 1, m = (l + r) / 2;
        while (l <= r) {
            int elMid = A.get(m);
            if (elMid == B) {
                ans = m;
                break;
            } else if (elMid > B) {
                r = m - 1;
            } else if (elMid < B) {
                l = m + 1;
            }
            m = (l + r) / 2;
        }
        return ans < 0 ? m + 1 : ans;
    }
